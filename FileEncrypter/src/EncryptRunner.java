        loadKeyStore(keyStoreFile, keyStorePass);
        this.plainInputFileName = plainTextFile;
        this.encryptedOutputFileName = encryptedOutput;
        this.keyAlias = keyAlias;
        this.keyPass = keyPass;
        this.keyStorePass = keyStorePass;
